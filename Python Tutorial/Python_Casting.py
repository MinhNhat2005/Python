# Chỉ định biến

# int
x = int(1)
y =int(2.5)
z = int("4")
print(x, type(x))
print(y, type(y))
print(z, type(z))
print("------------")

# float
x = float(1)
y = float(2.5)
z = float("3")
t = float("3.1")
print(x, type(x))
print(y, type(y))
print(z, type(z))
print(t, type(t))
print("------------")

# str
x = str("s1")
y = str(2)
z = str(3.0)
print(x, type(x))
print(y, type(y))
print(z, type(z))
